extension UITableViewCell {
    func enable(on: Bool) {
        for view in contentView.subviews {
            view.isUserInteractionEnabled = on
            view.alpha = on ? 1 : 0.35
        }
    }
}

extension UIView {
    func viewEnable(on: Bool) {
        for view in self.subviews {
            view.isUserInteractionEnabled = on
            view.alpha = on ? 1 : 0.35
        }
    }
}
